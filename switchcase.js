let starRating = 5;

switch(starRating){
    case 1:
        console.log ('Very Bad');
        break;
    case 2:
        console.log ('Bad');
        break;
    case 3:
        console.log ('Average');
        break;
    case 4:
        console.log ('Good');
        break;
    case 5:
        console.log ('Excellent');
        break;
    default:
        console.log ('Enter a valid integer between 1 and 5');
}


